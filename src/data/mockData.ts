import {
  ChangeDirectionType,
  Customer,
  CustomerResponseType,
  PossibilityType,
  SalesAction,
  TrustHistory,
} from "@/types/customer";

export type TimePeriodType = '1w' | '1m' | '6m' | '1y';
// 가능성 숫자 변환
export const POSSIBILITY_VALUE: Record<PossibilityType, number> = {
  "100%": 1.0,
  "90%": 0.9,
  "40%": 0.4,
  "0%": 0,
};
// 예상 매출 계산 (목표매출 * 가능성)
export const calculateExpectedRevenue = (
  targetRevenue: number | null | undefined,
  possibility: PossibilityType
): number => {
  if (!targetRevenue) return 0;
  return Math.round(targetRevenue * POSSIBILITY_VALUE[possibility]);
};
// 기간에 따른 일수 매핑
const PERIOD_DAYS: Record<TimePeriodType, number> = {
  "1w": 7,
  "1m": 30,
  "6m": 180,
  "1y": 365,
};
// salesActions에서 기간 내 가장 최신 액션의 가능성/고객반응 가져오기
interface PastAdoptionData {
  possibility: PossibilityType;
  customerResponse: CustomerResponseType;
  targetRevenue: number | null;
  targetDate: string | null;
  test: boolean;
  quote: boolean;
  approval: boolean;
  contract: boolean;
}

const getAdoptionFromSalesActions = (
  salesActions: SalesAction[] | undefined,
  currentAdoption: {
    possibility: PossibilityType;
    customerResponse: CustomerResponseType;
    targetRevenue?: number | null;
    targetDate?: string | null;
    test?: boolean;
    quote?: boolean;
    approval?: boolean;
    contract?: boolean;
  } | undefined,
  period: TimePeriodType
): PastAdoptionData => {
  // currentAdoption이 undefined인 경우 기본값 반환
  if (!currentAdoption) {
    return {
      possibility: "0%",
      customerResponse: "하",
      targetRevenue: null,
      targetDate: null,
      test: false,
      quote: false,
      approval: false,
      contract: false,
    };
  }

  const defaultResult: PastAdoptionData = {
    possibility: currentAdoption.possibility,
    customerResponse: currentAdoption.customerResponse,
    targetRevenue: currentAdoption.targetRevenue ?? null,
    targetDate: currentAdoption.targetDate ?? null,
    test: currentAdoption.test ?? false,
    quote: currentAdoption.quote ?? false,
    approval: currentAdoption.approval ?? false,
    contract: currentAdoption.contract ?? false,
  };

  if (!salesActions || salesActions.length === 0) {
    return defaultResult;
  }
  // 날짜 기준으로 정렬 (최신순)
  const sortedActions = [...salesActions]
    .filter((action) => action.possibility && action.customerResponse) // 가능성/고객반응이 있는 액션만
    .sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime());
  if (sortedActions.length === 0) {
    return defaultResult;
  }
  // 기간 내에서 가장 최신 액션 찾기
  const now = new Date("2024-12-10"); // 현재 날짜 기준
  const periodStart = new Date(now);
  periodStart.setDate(periodStart.getDate() - PERIOD_DAYS[period]);
  // 기간 시작 전의 가장 최신 액션 (기간 시작 시점의 상태)
  const actionBeforePeriod = sortedActions.find(
    (action) => new Date(action.date) <= periodStart
  );
  // 기간 시작 시점의 상태 반환
  if (actionBeforePeriod) {
    return {
      possibility: actionBeforePeriod.possibility!,
      customerResponse: actionBeforePeriod.customerResponse!,
      targetRevenue: actionBeforePeriod.targetRevenue ?? null,
      targetDate: actionBeforePeriod.targetDate ?? null,
      test: actionBeforePeriod.test ?? false,
      quote: actionBeforePeriod.quote ?? false,
      approval: actionBeforePeriod.approval ?? false,
      contract: actionBeforePeriod.contract ?? false,
    };
  }
  // 기간 시작 전 액션이 없으면 가장 오래된 액션 반환
  const oldestAction = sortedActions[sortedActions.length - 1];
  return {
    possibility: oldestAction.possibility!,
    customerResponse: oldestAction.customerResponse!,
    targetRevenue: oldestAction.targetRevenue ?? null,
    targetDate: oldestAction.targetDate ?? null,
    test: oldestAction.test ?? false,
    quote: oldestAction.quote ?? false,
    approval: oldestAction.approval ?? false,
    contract: oldestAction.contract ?? false,
  };
};
// trustHistory 키를 날짜 순으로 정렬하여 반환
const getSortedHistoryKeys = (history: TrustHistory | undefined): string[] => {
  if (!history) return [];
  return Object.keys(history)
    .filter((key) => history[key] !== undefined)
    .sort((a, b) => {
      // MMDD 형식으로 정렬 (같은 년도 내에서)
      const monthA = parseInt(a.slice(0, 2));
      const monthB = parseInt(b.slice(0, 2));
      const dayA = parseInt(a.slice(2));
      const dayB = parseInt(b.slice(2));
      // 11월 -> 12월 순으로 정렬 (월이 작으면 다음해로 간주)
      const yearAdjustA = monthA < 10 ? 12 : 0;
      const yearAdjustB = monthB < 10 ? 12 : 0;
      if (monthA + yearAdjustA !== monthB + yearAdjustB) {
        return monthA + yearAdjustA - (monthB + yearAdjustB);
      }
      return dayA - dayB;
    });
};
// 기간에 따른 주 수 매핑
const PERIOD_WEEKS: Record<TimePeriodType, number> = {
  "1w": 1,
  "1m": 4,
  "6m": 26,
  "1y": 52,
};
// 기간에 따른 변화량 계산
export const calculateChangeForPeriod = (
  history: TrustHistory | undefined,
  currentIndex: number | null | undefined,
  period: TimePeriodType
): { changeAmount: number; changeDirection: ChangeDirectionType } => {
  if (!history || currentIndex === null || currentIndex === undefined) {
    return { changeAmount: 0, changeDirection: "none" };
  }
  const sortedKeys = getSortedHistoryKeys(history);
  if (sortedKeys.length === 0) {
    return { changeAmount: 0, changeDirection: "none" };
  }
  const weeksBack = PERIOD_WEEKS[period];
  const currentKeyIndex = sortedKeys.length - 1;
  const targetKeyIndex = Math.max(0, currentKeyIndex - weeksBack);
  const targetKey = sortedKeys[targetKeyIndex];
  const targetData = history[targetKey];
  if (!targetData) {
    return { changeAmount: 0, changeDirection: "none" };
  }
  const change = currentIndex - targetData.trustIndex;
  return {
    changeAmount: Math.abs(change),
    changeDirection: change > 0 ? "up" : change < 0 ? "down" : "none",
  };
};
// 기간 시작 시점의 신뢰지수 가져오기
const getPastTrustIndex = (
  history: TrustHistory | undefined,
  currentIndex: number | null | undefined,
  period: TimePeriodType
): number | null => {
  if (!history || currentIndex === null || currentIndex === undefined) {
    return null;
  }
  const sortedKeys = getSortedHistoryKeys(history);
  if (sortedKeys.length === 0) {
    return null;
  }
  const weeksBack = PERIOD_WEEKS[period];
  const currentKeyIndex = sortedKeys.length - 1;
  const targetKeyIndex = Math.max(0, currentKeyIndex - weeksBack);
  const targetKey = sortedKeys[targetKeyIndex];
  const targetData = history[targetKey];
  return targetData?.trustIndex ?? null;
};
// 기간에 맞게 데이터 변환
export const getDataWithPeriodChange = (
  data: Customer[],
  period: TimePeriodType
): Customer[] => {
  return data.map((customer) => {
    const { changeAmount, changeDirection } = calculateChangeForPeriod(
      customer.trustHistory,
      customer.trustIndex,
      period
    );
    // adoptionDecision이 없는 경우 기본값 처리
    if (!customer.adoptionDecision) {
      return {
        ...customer,
        _periodData: {
          pastTrustIndex: customer.trustIndex ?? null,
          pastPossibility: "0%",
          pastCustomerResponse: "하",
          pastTargetRevenue: null,
          pastExpectedRevenue: 0,
          currentExpectedRevenue: 0,
          pastCurrentQuarterRevenue: 0,
          currentCurrentQuarterRevenue: 0,
          possibilityChange: "none",
          responseChange: "none",
          pastTest: false,
          pastQuote: false,
          pastApproval: false,
          pastContract: false,
          pastTargetDate: null,
        },
      };
    }

    // salesActions에서 기간 시작 시점의 가능성/고객반응 가져오기
    const pastAdoption = getAdoptionFromSalesActions(
      customer.salesActions,
      customer.adoptionDecision,
      period
    );
    // 현재 가능성/고객반응 (가장 최신 상태)
    const currentAdoption = {
      possibility: customer.adoptionDecision.possibility,
      customerResponse: customer.adoptionDecision.customerResponse,
      targetRevenue: customer.adoptionDecision.targetRevenue ?? null,
    };
    // 예상 매출 계산
    const currentExpectedRevenue = calculateExpectedRevenue(
      currentAdoption.targetRevenue,
      currentAdoption.possibility
    );
    const pastExpectedRevenue = calculateExpectedRevenue(
      pastAdoption.targetRevenue,
      pastAdoption.possibility
    );
    // 과거 신뢰지수 가져오기
    const pastTrustIndex = getPastTrustIndex(
      customer.trustHistory,
      customer.trustIndex,
      period
    );
    // 가능성 변화 계산
    const possibilityChange =
      POSSIBILITY_VALUE[currentAdoption.possibility] -
      POSSIBILITY_VALUE[pastAdoption.possibility];
    // 고객반응 변화 계산
    const responseOrder: Record<CustomerResponseType, number> = {
      상: 3,
      중: 2,
      하: 1,
    };
    const responseChange =
      responseOrder[currentAdoption.customerResponse] -
      responseOrder[pastAdoption.customerResponse];
    return {
      ...customer,
      changeAmount,
      changeDirection,
      adoptionDecision: {
        ...customer.adoptionDecision,
        // 예상 매출을 simulation 필드에 저장 (기존 호환성 유지)
        simulation:
          currentExpectedRevenue || customer.adoptionDecision.simulation,
      },
      // 기간 내 변화 정보 추가
      _periodData: {
        pastTrustIndex,
        pastPossibility: pastAdoption.possibility,
        pastCustomerResponse: pastAdoption.customerResponse,
        pastTargetRevenue: pastAdoption.targetRevenue,
        pastExpectedRevenue,
        currentExpectedRevenue,
        pastCurrentQuarterRevenue: 0,
        currentCurrentQuarterRevenue: 0,
        possibilityChange:
          possibilityChange > 0
            ? "up"
            : possibilityChange < 0
              ? "down"
              : "none",
        responseChange:
          responseChange > 0 ? "up" : responseChange < 0 ? "down" : "none",
        // 진행상태 과거값
        pastTest: pastAdoption.test,
        pastQuote: pastAdoption.quote,
        pastApproval: pastAdoption.approval,
        pastContract: pastAdoption.contract,
        // 목표일자 과거값
        pastTargetDate: pastAdoption.targetDate,
      },
    };
  });
};
